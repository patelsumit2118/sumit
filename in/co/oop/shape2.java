package in.co.oop;

public class shape2 {
	private int lenght;
	private int width;
	private int height;
	public shape2(int lenght, int width, int height) {
		
		this.lenght = lenght;
		this.width = width;
		this.height = height;
	}
	public int getLenght() {
		return lenght;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	
	

}
